let yearOfBirth = 1989;
let yearFuture = 2045;

// calculating the age
let age = yearFuture - yearOfBirth;
console.log(age);

// changing the future year to 2027
yearFuture = 2027;
age = yearFuture - yearOfBirth;
console.log(age);

// log out using string
console.log(`You will be ${age} in ${yearFuture}`);
